SHOW ONLYMASK 192.168.1.7
SHOW FIRSTIPLONG 192.168.1.7
SHOW FIRSTIPLONG 192.168.1.7/24
SHOW FIRSTIPLONG 192.168.1.7/16
SHOW FIRSTIP 192.168.1.7
SHOW FIRSTIP 192.168.1.7/24
SHOW FIRSTIP 192.168.1.7/16
SHOW LASTIPLONG 192.168.1.7
SHOW LASTIPLONG 192.168.1.7/24
SHOW LASTIPLONG 192.168.1.7/16
SHOW LASTIP 192.168.1.7
SHOW LASTIP 192.168.1.7/24
SHOW LASTIP 192.168.1.7/16
